package info.subbu.Dao.Entity;

public class Employee {
	private Integer sid;
	private String sname;
	private Double smaks;

	public Integer getSid() {
		return sid;
	}

	public void setSid(Integer sid) {
		this.sid = sid;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public Double getSmaks() {
		return smaks;
	}

	public void setSmaks(Double smaks) {
		this.smaks = smaks;
	}

}
